package com.lb.mobile.dto.requst;

import java.io.Serializable;

public class SchemePublishDto  implements Serializable {
    public  String uid;
    public  String schemeName;
    public  int schemeLotteryType;
    public  int schemePeriods;
    public  String schemeFull;
    public  String schemeGenrePd;
    public  String fileId;
    public  String schemeDec;

    public String getSchemeDec() {
        return schemeDec;
    }

    public void setSchemeDec(String schemeDec) {
        this.schemeDec = schemeDec;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getSchemeName() {
        return schemeName;
    }

    public void setSchemeName(String schemeName) {
        this.schemeName = schemeName;
    }

    public int getSchemeLotteryType() {
        return schemeLotteryType;
    }

    public void setSchemeLotteryType(int schemeLotteryType) {
        this.schemeLotteryType = schemeLotteryType;
    }

    public int getSchemePeriods() {
        return schemePeriods;
    }

    public void setSchemePeriods(int schemePeriods) {
        this.schemePeriods = schemePeriods;
    }

    public String getSchemeFull() {
        return schemeFull;
    }

    public void setSchemeFull(String schemeFull) {
        this.schemeFull = schemeFull;
    }

    public String getSchemeGenrePd() {
        return schemeGenrePd;
    }

    public void setSchemeGenrePd(String schemeGenrePd) {
        this.schemeGenrePd = schemeGenrePd;
    }

    public String getFileId() {
        return fileId;
    }

    public void setFileId(String fileId) {
        this.fileId = fileId;
    }
}
